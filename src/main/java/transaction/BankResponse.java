package transaction;


/**
 * 银行返回对象
 * @author sdwhy
 *
 */
public class BankResponse {

	private String code;
	
	private String message;
	
	

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
	
	
}
